CData::CData()
  :roll(0.0)
  ,pitch(0.0)
{}

///////////////////////////
///////////////////////////

CData& CData::operator=(const CData& other)
{
    if (this == &other) return *this;
    this->roll = other.roll;
    this->pitch = other.pitch;
    return *this;
}

